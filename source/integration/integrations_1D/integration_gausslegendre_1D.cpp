std::pair<DynVector<double>, DynVector<Point<1>>> GaussLegendre_1D::GetRule(const uint p) {
    if (p < 0 || p > 65) {
        printf("\n");
        printf("GAUSS LEGENDRE 1D - Fatal error!\n");
        printf("Illegal P = %d\n", p);
        exit(1);
    }

    std::pair<std::vector<double>, std::vector<Point<1>>> gp_data = this->GPData(this->GetNumGP(p));

    uint ngp = gp_data.first.size();

    std::pair<DynVector<double>, DynVector<Point<1>>> rule;
    rule.first.resize(ngp);
    rule.second.resize(ngp);

    for (uint gp = 0; gp < ngp; gp++) {
        rule.first[gp]  = gp_data.first[gp];
        rule.second[gp] = gp_data.second[gp];
    }

    return rule;
}

uint GaussLegendre_1D::GetNumGP(const uint p) {
    return (uint)ceil(((double)p + 1) / 2);
}

